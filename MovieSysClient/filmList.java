package MovieSysClient;

public class filmList {
    String film_id;
    String film_name;
    String film_poster;
    String rev_rate;
    String starpt;

    public String getFilm_id() {
        return film_id;
    }

    public void setFilm_id(String film_id) {
        this.film_id = film_id;
    }

    public String getFilm_name() {
        return film_name;
    }

    public void setFilm_name(String film_name) {
        this.film_name = film_name;
    }

    public String getFilm_poster() {
        return film_poster;
    }

    public void setFilm_poster(String film_poster) {
        this.film_poster = film_poster;
    }

    public String getRev_rate() {
        return rev_rate;
    }

    public void setRev_rate(String rev_rate) {
        this.rev_rate = rev_rate;
    }

    public String getStarpt() {
        return starpt;
    }

    public void setStarpt(String starpt) {
        this.starpt = starpt;
    }
}